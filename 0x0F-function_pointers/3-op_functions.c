/**
 *op_add - function to add two numbers
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - function to add two numbers
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - function to add two numbers
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - function to add two numbers
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - function to add two numbers
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
